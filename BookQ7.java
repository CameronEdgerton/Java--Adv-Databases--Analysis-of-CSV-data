package Data;

public class BookQ7 {

	private int id;
	private String title;

	public BookQ7(int id, String title) {
		this.id = id;
		this.title = title;
	}

	public int GetID() {
		return id;
	}

	public void SetID(int id) {
		this.id = id;
	}

	public String GetTitle() {
		return title;
	}

	public void SetTitle(String title) {
		this.title = title;
	}


}
